namespace Aspose {

namespace Page {

namespace Plugins {

/// <summary>
/// Represents possible types of data for plugin processing.
/// </summary>
enum class DataType
{
    /// <summary>
    /// Data type is a file represented by the path to it.
    /// </summary>
    File,
    /// <summary>
    /// Data type is a stream.
    /// </summary>
    Stream,
    /// <summary>
    /// Data type is a byte array.
    /// </summary>
    ByteArray
};

} // namespace Plugins
} // namespace Page
} // namespace Aspose


