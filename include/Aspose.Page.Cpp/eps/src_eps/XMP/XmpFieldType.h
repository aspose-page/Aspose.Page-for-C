namespace Aspose {

namespace Page {

namespace EPS {

namespace XMP {

/// <summary>
/// This enum represents types of a XMP field.
/// </summary>
enum class XmpFieldType
{
    /// <summary>
    /// This type represents a structure.
    /// </summary>
    Struct,
    /// <summary>
    /// This type represents a array.
    /// </summary>
    Array,
    /// <summary>
    /// This type represents a property.
    /// </summary>
    Property,
    /// <summary>
    /// This type represents a packet.
    /// </summary>
    Packet,
    /// <summary>
    /// This field represents an unknown type.
    /// </summary>
    Unknown
};

} // namespace XMP
} // namespace EPS
} // namespace Page
} // namespace Aspose


