#pragma once

namespace Aspose {

namespace Page {

/// <summary>
/// This enum contains possible values for text rendering mode.
/// </summary>
enum class TextRenderingMode
{
    /// <summary> Fill a text. </summary>
    Fill,
    /// <summary> Stroke a text. </summary>
    Stroke,
    /// <summary> Fill and stroke a text. </summary>
    FillAndStroke,
    /// <summary> Neither fill nor stroke a text. </summary>
    No
};

} // namespace Page
} // namespace Aspose


